// 8邻域相对位置relative position
int16_t relPos8[8] = {
	-((MD_W_MACRO_CNT)-1), -(MD_W_MACRO_CNT), -((MD_W_MACRO_CNT)+1), 
	        -1,                /* pos */               1,
	 ((MD_W_MACRO_CNT)-1),   MD_W_MACRO_CNT,   ((MD_W_MACRO_CNT)+1)
};


void printBuf(uint8_t *buf, uint32_t width, uint32_t height)
{
	int i, j, index;

	printf("\nFrameMACRO:\n");
	printf("  ");
	for (i = 0; i < width; i++)
	{
		printf("%2d ", i);
	}
	for (i = 0; i < height; i++)
	{
		printf("\n\r%2d", i);
		for (j = 0; j < width; j++)
		{
			index = i * width + j;
			printf("%2d ", buf[index]);
			//printf("%c ", buf[index] ? 'o' : ' ');
		}
	}
	printf("\n\n");
}

void markNeighbours(uint8_t *mdResult, uint16_t position, uint16_t markVal)
{
	/* 标记 */
	mdResult[position] = markVal;

    /* 8邻域查找并递归 */
	for (int i = 0; i < 8; i++)
	{
		if (mdResult[relPos8[i] + position] == 1)
		{
			markNeighbours(mdResult, (relPos8[i] + position), markVal);
		}
	}
	return;	
}

void binConnectAlgo(uint8_t *mdResult, uint32_t width, uint32_t height)
{
	int i, j;
	uint32_t index = 0; 
	uint32_t markVal = 2; // 标记从2开始改，因为0/1就是二值

	/* 对矩阵源从头到尾遍历一次 */
	for (i = 0; i < width; i++)
	{
		for (j = 0; j < height; j++)
		{
			index = i*height + j;

			if (mdResult[index] == 1)
			{
				markNeighbours(mdResult, index, markVal);
				markVal++;
			}
		}
	}

	/* 对连通域的起始编码调整为最小自然数 */
	for (i = 0; i < width; i++)
	{
		for (j = 0; j < height; j++)
		{
			index = i*height + j;

			if (mdResult[index] != 0 )
			{
				mdResult[index] = mdResult[index] - 1;
			}
		}
	}
}

int main(void)
{
	uint8_t mdResult[MD_MACRO_CNT] = {MD_MACRO_VAL};
	uint32_t width = MD_W_MACRO_CNT;
	uint32_t height = MD_H_MACRO_CNT;
	struct timeval t1, t2;


	printBuf(mdResult, width, height);

	gettimeofday(&t1, NULL);
	binConnectAlgo(mdResult, width, height);
	gettimeofday(&t2, NULL);

	printBuf(mdResult, width, height);

	printf("take time: %lu ms (%lu us)\n", (t2.tv_sec-t1.tv_sec)*1000+(t2.tv_usec-t1.tv_usec)/1000,
										   (t2.tv_sec-t1.tv_sec)*1000000+(t2.tv_usec-t1.tv_usec));
	return 0;
}

